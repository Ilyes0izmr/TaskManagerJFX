package com.example.todolist.model;

/**
 * Represents the reminder intervals for tasks in the to-do list application.
 * The possible reminder intervals are:
 * <ul>
 *   <li>DAILY</li>
 *   <li>WEEKLY</li>
 *   <li>MONTHLY</li>
 * </ul>
 * These values specify how frequently a task's reminder should be triggered.
 *
 * @author Meftah Mohamed
 */
public enum Reminder {
    DAILY, WEEKLY, MONTHLY
}
